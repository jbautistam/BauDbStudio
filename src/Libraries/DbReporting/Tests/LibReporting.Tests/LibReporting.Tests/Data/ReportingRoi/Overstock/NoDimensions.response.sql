WITH
GroupedCte AS 
(
SELECT 			   
						   SUM(Overstock.ProjectionVariationPercentage) AS ProjectionVariationPercentage, 
 SUM(Overstock.ActualStock) AS ActualStock, 
 SUM(Overstock.WarehouseStock) AS WarehouseStock, 
 SUM(Overstock.Overstock12) AS Overstock12, 
 SUM(Overstock.OverstockCost12) AS OverstockCost12, 
 SUM(Overstock.Overstock28) AS Overstock28, 
 SUM(Overstock.OverstockCost28) AS OverstockCost28, 
 SUM(Overstock.Overstock53) AS Overstock53, 
 SUM(Overstock.OverstockCost53) AS OverstockCost53, 
 SUM(Overstock.PointOfSaleImminentPendingReceptions) AS PointOfSaleImminentPendingReceptions, 
 SUM(Overstock.PointOfSalePendingReceptions) AS PointOfSalePendingReceptions, 
 SUM(Overstock.WarehouseImminentPendingReceptions) AS WarehouseImminentPendingReceptions, 
 SUM(Overstock.WarehousePendingReceptions) AS WarehousePendingReceptions, 
 SUM(Overstock.PointsOfSaleWithStock) AS PointsOfSaleWithStock, 
 SUM(Overstock.PointsOfSaleInAssortment) AS PointsOfSaleInAssortment, 
 SUM(Overstock.PointsOfSaleBlocked) AS PointsOfSaleBlocked, 
 SUM(Overstock.OriginalOrder) AS OriginalOrder, 
 SUM(Overstock.Sales4WeeksAgo) AS Sales4WeeksAgo, 
 SUM(Overstock.Sales3WeeksAgo) AS Sales3WeeksAgo, 
 SUM(Overstock.Sales2WeeksAgo) AS Sales2WeeksAgo, 
 SUM(Overstock.Sales1WeekAgo) AS Sales1WeekAgo, 
 SUM(Overstock.Stock4WeeksAgo) AS Stock4WeeksAgo, 
 SUM(Overstock.Stock3WeeksAgo) AS Stock3WeeksAgo, 
 SUM(Overstock.Stock2WeeksAgo) AS Stock2WeeksAgo, 
 SUM(Overstock.Stock1WeekAgo) AS Stock1WeekAgo						   
						FROM Fact.Overstock
		            	
						
)
SELECT 
							ProjectionVariationPercentage, 
 ActualStock, 
 WarehouseStock, 
 Overstock12, 
 OverstockCost12, 
 Overstock28, 
 OverstockCost28, 
 Overstock53, 
 OverstockCost53, 
 PointOfSaleImminentPendingReceptions, 
 PointOfSalePendingReceptions, 
 WarehouseImminentPendingReceptions, 
 WarehousePendingReceptions, 
 PointsOfSaleWithStock, 
 PointsOfSaleInAssortment, 
 PointsOfSaleBlocked, 
 OriginalOrder, 
 Sales4WeeksAgo, 
 Sales3WeeksAgo, 
 Sales2WeeksAgo, 
 Sales1WeekAgo, 
 Stock4WeeksAgo, 
 Stock3WeeksAgo, 
 Stock2WeeksAgo, 
 Stock1WeekAgo,
 COUNT(*) OVER () AS TotalCount, 
 SUM(ProjectionVariationPercentage) OVER() AS TotalProjectionVariationPercentage, 
 SUM(ActualStock) OVER() AS TotalActualStock, 
 SUM(WarehouseStock) OVER() AS TotalWarehouseStock, 
 SUM(Overstock12) OVER() AS TotalOverstock12, 
 SUM(OverstockCost12) OVER() AS TotalOverstockCost12, 
 SUM(Overstock28) OVER() AS TotalOverstock28, 
 SUM(OverstockCost28) OVER() AS TotalOverstockCost28, 
 SUM(Overstock53) OVER() AS TotalOverstock53, 
 SUM(OverstockCost53) OVER() AS TotalOverstockCost53, 
 SUM(PointOfSaleImminentPendingReceptions) OVER() AS TotalPointOfSaleImminentPendingReceptions, 
 SUM(PointOfSalePendingReceptions) OVER() AS TotalPointOfSalePendingReceptions, 
 SUM(WarehouseImminentPendingReceptions) OVER() AS TotalWarehouseImminentPendingReceptions, 
 SUM(WarehousePendingReceptions) OVER() AS TotalWarehousePendingReceptions, 
 SUM(PointsOfSaleWithStock) OVER() AS TotalPointsOfSaleWithStock, 
 SUM(PointsOfSaleInAssortment) OVER() AS TotalPointsOfSaleInAssortment, 
 SUM(PointsOfSaleBlocked) OVER() AS TotalPointsOfSaleBlocked, 
 SUM(OriginalOrder) OVER() AS TotalOriginalOrder, 
 SUM(Sales4WeeksAgo) OVER() AS TotalSales4WeeksAgo, 
 SUM(Sales3WeeksAgo) OVER() AS TotalSales3WeeksAgo, 
 SUM(Sales2WeeksAgo) OVER() AS TotalSales2WeeksAgo, 
 SUM(Sales1WeekAgo) OVER() AS TotalSales1WeekAgo, 
 SUM(Stock4WeeksAgo) OVER() AS TotalStock4WeeksAgo, 
 SUM(Stock3WeeksAgo) OVER() AS TotalStock3WeeksAgo, 
 SUM(Stock2WeeksAgo) OVER() AS TotalStock2WeeksAgo, 
 SUM(Stock1WeekAgo) OVER() AS TotalStock1WeekAgo
						FROM GroupedCte
						
						